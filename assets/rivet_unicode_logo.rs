pub fn render_rivet_logo(frame: &mut Frame, area: Rect) {
    let widget = Paragraph::new(RIVET_LOGO_BRAILLE)
        .alignment(Alignment::Center)
        .wrap(Wrap { trim: false });

    frame.render_widget(widget, area);
}
